# Helper method to run specific year seeds
def seed_year(year)
  file = BURNING_MAN_YEARS[year][:file]
  path = Rails.root.join("db", "seeds", file)
  
  if File.exist?(path)
    puts "Loading #{file}..."
    load path
  else
    puts "‚ö†Ô∏è  Seed file not found: #{file}"
    puts "   Create it with comprehensive data for #{year}"
  end
end

# Helper to seed all complete years
def seed_all_complete_years
  BURNING_MAN_YEARS.each do |year, info|
    if info[:status] == "‚úÖ COMPLETE"
      seed_year(year)
    end
  end
end

# Helper to show progress
def show_seed_progress
  total = BURNING_MAN_YEARS.count
  complete = BURNING_MAN_YEARS.count { |_, info| info[:status] == "‚úÖ COMPLETE" }
  
  puts "\nüìä Burning Man History Seed Progress"
  puts "=" * 50
  puts "Total years: #{total}"
  puts "Complete: #{complete} (#{(complete.to_f / total * 100).round(1)}%)"
  puts "Remaining: #{total - complete}"
  puts "\nComplete years:"
  BURNING_MAN_YEARS.each do |year, info|
    puts "  ‚úÖ #{year}" if info[:status] == "‚úÖ COMPLETE"
  end
end

